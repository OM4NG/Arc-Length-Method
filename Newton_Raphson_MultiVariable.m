clear all
clc

x=[1;2];

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

F=[ (x(1)^2 - x(2));
    (x(1)^2 + x(2)^2 -4) ];

J=[2*x(1)   -1;
   2*x(1)   2*x(2) ];

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

for i=1:1000
    
    F=[ (x(1)^2 - x(2));
        (x(1)^2 + x(2)^2 -4) ];

    J=[2*x(1)   -1;
       2*x(1)   2*x(2) ];
   
    x_new = x - (J^-1)*F;
    
    x
    e = norm(x_new-x)
    table(i,:)=[x(1) x(2) e]; 
    x=x_new;
    
    if(e < 10^-5)
        break;
    end
       
end
table

